package Array_for;

public class Main {

    public static void main(String[] args) {

        //definindo valores ao array
        int[] num = {10, 20, 30, 40, 50};

        //percorrendo os valores com for e saindo com os dados
        for (int i = 0; i < num.length; i++) {
            System.out.println(num[i]);
        }


    }
}
